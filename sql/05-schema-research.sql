CREATE TABLE research.analysts (
    -- analyst profile (assuming it will be very small since local)
    analyst_id      UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    username        TEXT NOT NULL UNIQUE,
    email           TEXT NOT NULL UNIQUE,
    pswd_hash       TEXT NOT NULL,
    pswd_salt       TEXT NOT NULL,
    pswd_date       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- SUBJECT AND METADATA
CREATE TABLE research.athletes (
    -- might be useful for mapping across different datasets with differing IDs
    athlete_id          UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    external_code       TEXT NOT NULL, -- TODO: define required format for this code
    dataset_id          UUID references research.datasets(dataset_id) on delete cascade,
    birth_date          DATE,
    birth_year          INTEGER,
    age_at_observation  SMALLINT, -- if birth_date not available; trigger updated after a year
    age_logged_at       TIMESTAMPTZ DEFAULT NOW(),
    notes               TEXT,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CHECK (
        external_code ~ '^R[0-9]{1}[A-Z0-9]{3}$'
    ),
    CHECK (
        birth_date IS NOT NULL
        OR birth_year IS NOT NULL
        OR age_at_observation IS NOT NULL
    ),
    CHECK ( -- avoids contradictory inputs
    birth_date IS NULL
    OR birth_year IS NULL
    OR EXTRACT(YEAR FROM birth_date) = birth_year
)
);

CREATE TABLE research.datasets (
    -- linking table pointing to different kinds of datasets
    dataset_id          UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    uploaded_by_id      UUID NOT NULL REFERENCES research.analysts(analyst_id) ON DELETE RESTRICT,
    original_file_name  TEXT NOT NULL,
    stored_relative_path TEXT NOT NULL,
    content_hash        TEXT,
    uploaded_at         TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    import_status       TEXT NOT NULL DEFAULT 'uploaded',
    notes               TEXT
);

