-- FUNCTIONS
-- DOMAIN
CREATE OR REPLACE FUNCTION research.get_age(
    -- Sets precedence: birth_date > birth_year > age
    p_birth_date DATE,
    p_birth_year INTEGER,
    p_age INTEGER,
    p_reference_date DATE
)
RETURNS INTEGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF p_birth_date IS NOT NULL THEN
        RETURN EXTRACT(YEAR FROM AGE(p_reference_date, p_birth_date));
    ELSIF p_birth_year IS NOT NULL THEN
        RETURN EXTRACT(YEAR FROM p_reference_date) - p_birth_year;
    ELSE
        RETURN p_age;
    END IF;
END;
$$;
